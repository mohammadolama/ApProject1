package Configs;

public class ShopConfig {
    private int startX1;
    private int startY1;
    private int spacing1;

    private int startX2;
    private int startY2;
    private int width;
    private int height;

    private int priceY;

    public ShopConfig() {
    }

    public int getStartX1() {
        return startX1;
    }

    public void setStartX1(int startX1) {
        this.startX1 = startX1;
    }

    public int getStartY1() {
        return startY1;
    }

    public void setStartY1(int startY1) {
        this.startY1 = startY1;
    }

    public int getSpacing1() {
        return spacing1;
    }

    public void setSpacing1(int spacing1) {
        this.spacing1 = spacing1;
    }

    public int getStartX2() {
        return startX2;
    }

    public void setStartX2(int startX2) {
        this.startX2 = startX2;
    }

    public int getStartY2() {
        return startY2;
    }

    public void setStartY2(int startY2) {
        this.startY2 = startY2;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getPriceY() {
        return priceY;
    }

    public void setPriceY(int priceY) {
        this.priceY = priceY;
    }
}