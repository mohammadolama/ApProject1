package Configs;

public class SummonedConfig {

    private int width = 350;
    private int height = 400;

    private int damageX = 40;
    private int hpX = 290;
    private int damageYl = 380;

    public SummonedConfig() {
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getDamageX() {
        return damageX;
    }

    public void setDamageX(int damageX) {
        this.damageX = damageX;
    }

    public int getHpX() {
        return hpX;
    }

    public void setHpX(int hpX) {
        this.hpX = hpX;
    }

    public int getDamageYl() {
        return damageYl;
    }

    public void setDamageYl(int damageYl) {
        this.damageYl = damageYl;
    }
}
